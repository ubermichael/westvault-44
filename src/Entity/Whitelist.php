namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;
use Nines\UtilBundle\Entity\AbstractEntity;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * Providers which meet the minimum requirements are whitelisted.
 *
 * @ORM\Entity(repositoryClass="App\Repository\WhitelistRepository")
 * @ORM\Table(indexes={
 *     @ORM\Index(columns={"uuid", "comment"}, flags={"fulltext"}),
 *     @ORM\Index(columns={"uuid"})
 * })
 */
class Whitelist extends AbstractEntity {
    /**
     * Provider UUID, as generated by the PLN plugin. This cannot be part
     * of a relationship - a provider may be listed before we have
     * a record of it.
     *
     * @var string
     * @Assert\Uuid(strict=false)
     * @ORM\Column(type="string", length=36, nullable=false)
     */
    private $uuid;

    /**
     * Short message describing why the provider was listed.
     *
     * @var string
     * @ORM\Column(type="text")
     */
    private $comment;

    /**
     * Build a new, empty whitelist entry.
     */
    public function __construct() {
        parent::__construct();
    }

    /**
     * Use the white list entry's uuid as a string representation.
     */
    public function __toString() : string {
        return $this->uuid;
    }

    public function getUuid() : ?string {
        return $this->uuid;
    }

    public function setUuid(string $uuid) : self {
        $this->uuid = $uuid;

        return $this;
    }

    public function getComment() : ?string {
        return $this->comment;
    }

    public function setComment(string $comment) : self {
        $this->comment = $comment;

        return $this;
    }
}
