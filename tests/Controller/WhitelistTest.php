    /**
     * @group admin
     * @group edit
     */
    public function testAdminEdit() : void {
        $this->login('user.admin');
        $formCrawler = $this->client->request('GET', '/whitelist/1/edit');
        $this->assertSame(Response::HTTP_OK, $this->client->getResponse()->getStatusCode());

        $form = $formCrawler->selectButton('Save')->form([
            'whitelist[uuid]' => '35983783-9CF6-493B-BAE5-A19A45A2DFF0',
            'whitelist[comment]' => 'Updated Comment',
        ]);

        $this->client->submit($form);
        $this->assertTrue($this->client->getResponse()->isRedirect('/whitelist/1'));
        $responseCrawler = $this->client->followRedirect();
        $this->assertSame(Response::HTTP_OK, $this->client->getResponse()->getStatusCode());
        $this->assertSame(1, $responseCrawler->filter('td:contains("35983783-9CF6-493B-BAE5-A19A45A2DFF0")')->count());
        $this->assertSame(1, $responseCrawler->filter('td:contains("Updated Comment")')->count());
    }

