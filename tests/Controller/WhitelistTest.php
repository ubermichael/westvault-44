    public function testAnonSearch() : void {
        $repo = $this->createMock(WhitelistRepository::class);
        $repo->method('searchQuery')->willReturn([$this->getReference('whitelist.1')]);
        $this->client->disableReboot();
        $this->client->getContainer()->set('test.' . WhitelistRepository::class, $repo);

        $crawler = $this->client->request('GET', '/whitelist/search');
        $this->assertSame(self::ANON_RESPONSE_CODE, $this->client->getResponse()->getStatusCode());
        if (self::ANON_RESPONSE_CODE === Response::HTTP_FOUND) {
            // If authentication is required stop here.
            return;
        }

        $form = $crawler->selectButton('btn-search')->form([
            'q' => 'whitelist',
        ]);

        $responseCrawler = $this->client->submit($form);
        $this->assertSame(200, $this->client->getResponse()->getStatusCode());
    }

